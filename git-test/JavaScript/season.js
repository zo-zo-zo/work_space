const season = "夏" // 自由に値を変更してみましょう。
switch (season) {
  case "春": // seasonが春の場合
    console.log("花見にいきます");
  case "夏":　// seasonが夏の場合
    console.log("プールにいきます");
  case "秋": // seasonが秋の場合
    console.log("栗を拾います");
  case "冬": // seasonが冬の場合
    console.log("餅を食べます");
  default:
    console.log("勉強をします")
}
